package com.cm.user.bein;

public class ComptePersmissions {
	
	private String codeModule;
	private String codeSmodule;
	private String codeFonctionnalite;
	
	public ComptePersmissions() {
		super();
		
	}

	public String getCodeModule() {
		return codeModule;
	}

	public void setCodeModule(String codeModule) {
		this.codeModule = codeModule;
	}

	public String getCodeSmodule() {
		return codeSmodule;
	}

	public void setCodeSmodule(String codeSmodule) {
		this.codeSmodule = codeSmodule;
	}

	public String getCodeFonctionnalite() {
		return codeFonctionnalite;
	}

	public void setCodeFonctionnalite(String codeFonctionnalite) {
		this.codeFonctionnalite = codeFonctionnalite;
	}
    
	
}
